    scoreShoot: Boolean,
    scorePickup: Boolean,
    scoreOther: Boolean,
    scoreOtherExplain: String,
    pickupGround: Boolean,
    pickupSource: Boolean,
    pickupOther: Boolean,
    pickupOtherExplain: String,
    auto: Boolean,
    autoCount: String,
    idealAuto: String,
    canScoreSpeaker: Boolean,
    canScoreAmp: Boolean,
    canScoreTrap: Boolean,
